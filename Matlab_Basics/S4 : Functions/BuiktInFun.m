%create vector of numbers
V = [1 7 3 6 5 9];

%find the maximum number 
maximumv = max(V);
disp(['The maximum number is: ', num2str(maximumv)]);

%find the minimum number 
minimumv = min(V);
disp(['The minimum number is: ', num2str(minimumv)]);

%calculate the sum of all numbers
total = sum(V);
disp(['The sum of numbers is: ', num2str(total)]);
